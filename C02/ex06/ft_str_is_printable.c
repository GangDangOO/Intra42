#include <unistd.h>

int		ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0')
	{
		return 1;
	}
	while (str[i] != '\0')
	{
		if (!(str[i] >= 41 && str[i] <= 176))
		{
			return (0);
		}
	}
	return (1);
}
